let currentLanguage = 'pt_BR';

// Detect system language
function detectLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const langMap = {
        'en': 'en',
        'es': 'es',
        'zh': 'zh',
        'ja': 'ja',
        'de': 'de',
        'it': 'it',
        'pt-BR': 'pt_BR',
        'pt': 'pt',
        'fr': 'fr'
    };
    
    // Try exact match first
    if (langMap[browserLang]) {
        return langMap[browserLang];
    }
    
    // Try language code only (e.g., 'pt' from 'pt-PT')
    const langCode = browserLang.split('-')[0];
    if (langMap[langCode]) {
        return langMap[langCode];
    }
    
    return 'pt_BR'; // Default
}

// Load language preference from localStorage
function loadLanguage() {
    const saved = localStorage.getItem('habitus_language');
    if (saved && translations[saved]) {
        currentLanguage = saved;
    } else {
        currentLanguage = detectLanguage();
    }
    return currentLanguage;
}

// Save language preference
function saveLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('habitus_language', lang);
}

// Get translation
function t(key) {
    return translations[currentLanguage]?.[key] || translations['pt_BR'][key] || key;
}

// Update all UI text
function updateUI() {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });
    
    // Update title
    document.title = `${t('appTitle')} - ${t('tasks')}`;
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
        const key = el.getAttribute('data-i18n-aria-label');
        el.setAttribute('aria-label', t(key));
    });
}

// Initialize i18n
function initI18n() {
    loadLanguage();
    const selector = document.getElementById('language-selector');
    if (selector) {
        selector.value = currentLanguage;
        selector.addEventListener('change', (e) => {
            saveLanguage(e.target.value);
            if (typeof window.updateUI === 'function') {
                window.updateUI();
            } else {
                updateUI();
                if (typeof RenderManager !== 'undefined' && RenderManager.renderAll) {
                    RenderManager.renderAll();
                }
            }
        });
    }
    updateUI();
}
